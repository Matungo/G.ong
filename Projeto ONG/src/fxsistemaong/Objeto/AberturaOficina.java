package fxsistemaong.Objeto;

/**
 *
 * @author Macbook
 */
public class AberturaOficina {
   
    private int codOficina;
    private String nome, instrutor, diaSemana, horarioComeca, horarioTermina;

    public int getCodOficina() {
        return codOficina;
    }

    public void setCodOficina(int codOficina) {
        this.codOficina = codOficina;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getInstrutor() {
        return instrutor;
    }

    public void setInstrutor(String instrutor) {
        this.instrutor = instrutor;
    }

    public String getDiaSemana() {
        return diaSemana;
    }

    public void setDiaSemana(String diaSemana) {
        this.diaSemana = diaSemana;
    }

    public String getHorarioComeca() {
        return horarioComeca;
    }

    public void setHorarioComeca(String horarioComeca) {
        this.horarioComeca = horarioComeca;
    }

    public String getHorarioTermina() {
        return horarioTermina;
    }

    public void setHorarioTermina(String horarioTermina) {
        this.horarioTermina = horarioTermina;
    }
    
    
}
